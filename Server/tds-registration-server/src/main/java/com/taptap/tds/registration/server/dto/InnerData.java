package com.taptap.tds.registration.server.dto;

public class InnerData {
    private int no;
    private String si;
    private int bt;
    private long ot;
    private int ct;
    private String di;
    private String pi;

    public InnerData() {
    }

    public int getNo() {
        return this.no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getSi() {
        return this.si;
    }

    public void setSi(String si) {
        this.si = si;
    }

    public int getBt() {
        return this.bt;
    }

    public void setBt(int bt) {
        this.bt = bt;
    }

    public long getOt() {
        return this.ot;
    }

    public void setOt(long ot) {
        this.ot = ot;
    }

    public int getCt() {
        return this.ct;
    }

    public void setCt(int ct) {
        this.ct = ct;
    }

    public String getDi() {
        return this.di;
    }

    public void setDi(String di) {
        this.di = di;
    }

    public String getPi() {
        return this.pi;
    }

    public void setPi(String pi) {
        this.pi = pi;
    }
}
